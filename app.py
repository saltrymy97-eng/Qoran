# 4. الذكاء الاصطناعي
# ==========================================
def ask_ai(question, category=None, chat_history=None):
    if chat_history is None:
        chat_history = []

    data = load_data()
    
    # بناء السياق حسب الفئة
    if category and category in data:
        context = data[category]
    else:
        context = "\n\n".join([f"{k}: {v}" for k, v in data.items()])
    
    if not context.strip():
        return "لا توجد بيانات متاحة."

    system_prompt = SYSTEM_PROMPT.format(context=context[:2000])
    messages = [{"role": "system", "content": system_prompt}]
    if chat_history:
        messages.extend(chat_history[-4:])
    messages.append({"role": "user", "content": question})

    client = get_client()
    if not client:
        return context[:500]

    try:
        response = client.chat.completions.create(
            model="llama-3.3-70b-versatile",
            messages=messages,
            temperature=0.3,
            max_tokens=400
        )
        answer = response.choices[0].message.content
        log_question(question, category or "info", source="groq")
        return answer
    except Exception:
        log_question(question, category or "info", source="fallback")
        return context[:500]

